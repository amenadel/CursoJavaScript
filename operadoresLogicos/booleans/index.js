                                          //BOOLEANS OPERADORES COMPARATIVOS

                                        // Maior que >
                                        // Menor que <
                                        // Maior ou igual >=
                                        // Menor que <= 
                                        // Igual ==
                                        // Diferente != 
                                        // Identico ===

                                        console.log(1 > 2)
                                        console.log(5 < 10)
                                        console.log(3 >= 3)
                                        console.log(5 <= 4)
                                        console.log(5 == 4)
                                        console.log("Matheus" != "Matheus")
                                        console.log(3 === '3')
                                        console.log(4 === 4)

                                        //BOOLEANS OPERADORES LOGICOS

                                        //Por meio de uma comparação resultam em um Boolean
                                        //&& - and -> para ser true, os dois 'lados' da comparação precisam ser true
                                        //|| -  or -> para ser true, basta um dos lados da comparação ser true
                                        // ! - not -> inverter os valores (true vira false)
                                        console.log( 5 > 3 && 3 == 2)
                   